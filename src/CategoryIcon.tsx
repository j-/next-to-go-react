/**
 * Lots of ways to render category icons. For a rapid prototype this is good
 * enough.
 */
export const CategoryIcon: FC<CategoryIconProps> = ({
  categoryId,
  ...props
}) => {
  const path = useMemo(() => {
    switch (categoryId) {
    case RacingCategory.GREYHOUND:
      return pathGreyhound;

    case RacingCategory.HARNESS:
      return pathHarness;

    // NOTE: Using Horse icon as default in case results come back with
    // an unexpected category ID. Should not happen but if it does then
    // at least something is shown.
    case RacingCategory.HORSE:
    default:
      return pathHorse;
    }
  }, [categoryId]);

  const label = useMemo(() => {
    switch (categoryId) {
    case RacingCategory.GREYHOUND:
      return 'Greyhound race';

    case RacingCategory.HARNESS:
      return 'Harness race';

    case RacingCategory.HORSE:
      return 'Horse race';

    default:
      return 'Other race';
    }
  }, [categoryId]);

  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      viewBox="0 0 1500 1500"
      fill="currentColor"
      aria-label={label}
      {...props}
    >
      {path}
    </svg>
  );
};
